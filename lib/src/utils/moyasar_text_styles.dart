/// Centralized font family constant for the Moyasar SDK.
///
/// All widgets should reference [MoyasarStyles.fontFamily] instead of
/// hardcoding the font family string. This ensures consistency across all
/// screens and makes future font changes trivial.
class MoyasarStyles {
  MoyasarStyles._();

  /// The primary font family used throughout the SDK.
  static const String fontFamily = 'ReadexPro';
}